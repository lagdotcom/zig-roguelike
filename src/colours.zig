pub const Black: RGB8 = .{ .r = 0, .g = 0, .b = 0 };

pub const LightBlue: RGB8 = .{ .r = 50, .g = 50, .b = 100 };
pub const DarkBlue: RGB8 = .{ .r = 0, .g = 0, .b = 100 };
pub const LightYellow: RGB8 = .{ .r = 200, .g = 180, .b = 50 };
pub const DarkYellow: RGB8 = .{ .r = 130, .g = 110, .b = 50 };
